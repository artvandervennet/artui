/**
 * Copies registry.json into apps/docs/public/ so Next.js serves it as a
 * static asset at /registry.json (always "latest"), writes an immutable
 * snapshot at /registry/v<version>/registry.json so the CLI can pin to a
 * specific release, and emits apps/docs/lib/registry-version.ts so the
 * docs UI can render the current version without a runtime JSON import.
 *
 * Runs in the docs site's `prebuild` step. Turborepo's ^build dependency
 * ensures @artui/registry has produced registry.json by the time this runs.
 */
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const src = resolve(here, '../../../registry/registry.json');
const publicDir = resolve(here, '../public');
const latestDest = resolve(publicDir, 'registry.json');
const libDest = resolve(here, '../lib/registry-version.ts');

const raw = readFileSync(src, 'utf8');
const { version } = JSON.parse(raw);
if (!version) {
  throw new Error(`[copy-registry] ${src} has no "version" field`);
}

const versionedDest = resolve(publicDir, 'registry', `v${version}`, 'registry.json');

mkdirSync(dirname(latestDest), { recursive: true });
mkdirSync(dirname(versionedDest), { recursive: true });
mkdirSync(dirname(libDest), { recursive: true });
writeFileSync(latestDest, raw);
writeFileSync(versionedDest, raw);
writeFileSync(
  libDest,
  `// Generated by apps/docs/scripts/copy-registry.mjs. Do not edit.\nexport const REGISTRY_VERSION = '${version}';\n`,
);
console.log(`[copy-registry] ${src} → ${latestDest}`);
console.log(`[copy-registry] ${src} → ${versionedDest}`);
console.log(`[copy-registry] wrote ${libDest}`);
